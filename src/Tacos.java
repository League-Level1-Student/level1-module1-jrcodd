
public class Tacos {
private String meat;
private String sauce;
public Tacos(String meat, String sauce) {
	this.meat = meat;
	this.sauce = sauce;
}
public String getMeat() {
return meat;	
}
public String getSauce() {
	return sauce;
}
public void setMeat(String newmeat) {
	this.meat = newmeat;
	
}
public void setSauce(String news) {
	this.sauce = news;
}

}
